type PipeFns<
  P,
  Fns extends UnaryFunction[],
  Prev extends UnaryFunction[] = [never, ...Fns],
> = {
  [I in keyof Fns]: Fns[I] extends UnaryFunction
    ? I extends '0'
      ? (arg: P) => ReturnType<Fns[I]>
      : (
          arg: I extends keyof Prev ? ReturnType<Prev[I]> : unknown,
        ) => ReturnType<Fns[I]>
    : Fns[I]
}

